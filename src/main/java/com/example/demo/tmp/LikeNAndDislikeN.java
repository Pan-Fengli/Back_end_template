package com.example.demo.tmp;

public class LikeNAndDislikeN {
    private final int likeN;
    private final int dislikeN;

    public LikeNAndDislikeN(int like, int dislike) {
        likeN = like;
        dislikeN = dislike;
    }

    public int getLikeN() {
        return likeN;
    }

    public int getDislikeN() {
        return dislikeN;
    }
}
